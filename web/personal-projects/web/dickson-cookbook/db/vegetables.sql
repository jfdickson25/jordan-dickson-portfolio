    flesh in the blender to puree.  This works the blender pretty hard, so it’s not for a wimpy blender.  It works best if you start on a slow 
    speed until the bottom part starts to puree, then gradually increase the speed until you get to the puree setting.  Let it run until it is 
    really smooth.  At this point, you can use it for your favorite recipe or save it for later.  I usually pack 2 cups (most of my recipes call 
    for a large can, which is pretty much 2 cups) in a pint size mason uar and freeze it.  You could actually can it yourself, but I use mine up 
    before the holidays are over, so I don’t go to that much trouble.  It will keep for a few days in the fridge, but if you need it to last 
    longer than that, just pop it in an airtight container or freezer bag and stick it in the freezer for a few months.    
    2   3lb sugar pumpkins will yield about 5 pint jars of puree.', 2);

