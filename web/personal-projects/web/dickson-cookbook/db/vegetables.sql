    INSERT INTO recipe (title, ingredients, instructions, category_id) VALUES ('Pumpkin Puree', 
    'NA', 
    'You will want to start with a 2-4 pound sugar pumpkin (they work best for roasting because they are smaller than carving pumpkins, 
    but really, any old pumpkin will do).  Cut in half, scoop out the seeds and stringy stuff and lay them face down on a cookie sheet.  
    Place the cookie sheet in the oven at 350-375.  Depending on the size of the pumpkins it will take 45-60 minutes to roast.  You will 
    know when they are done because the skin of the pumpkin will get wrinkly.  If you can easily stick a fork in it, then it’s probably done.  
